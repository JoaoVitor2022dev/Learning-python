# Precisamos coletar os dados de entrada de faltas e presenças para verificamos; 

Faltas = int(input(" 1 - Quantas faltas no dia 15 de janeiro até o dia 15 de ferveiro ? "))


Presenca = int(input(" 2 - Quntas presenças tivemos no 15 de janeiro até o dia 15 de ferveiro ? "))

# fazer o calculado de abs desse período de tempo

abs = float(Faltas / Presenca)

print(abs)


