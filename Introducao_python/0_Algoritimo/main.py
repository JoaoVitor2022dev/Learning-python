# algoritimo para aumentar o salario do funcionario 

SalarioIncial = int(input("Digite o sálario do funcionário: "))

PorcentualAumento = int(input("Digite o porcentual de aumento: "))

# VAMOS FAZER O CALCULO PARA O NOVO SALARIO 

Aumento = SalarioIncial * (PorcentualAumento / 100)

NovoSalario = aumento + SalarioIncial

print("Salário inicial: " , SalarioIncial)
print("Novo salario: ", NovoSalario)

